class DataProcessor:
    def __init__(self, data):
        self.data = data

    def process(self):
        result = []
        for item in self.data:
            if self.validate(item):
                result.append(self.transform(item))
        return result

    def validate(self, item):
        return item is not None

    def transform(self, item):
        return str(item).upper()

